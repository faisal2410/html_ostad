// Button: The < button > tag creates a clickable button on a webpage.It is often used to trigger some kind of action when clicked.
//     Example:


//     < button > Click me</ >
//         Input text and password: The < input > tag is used to create various form elements such as text fields, checkboxes, and radio buttons.The type attribute is used to specify the type of input element.For example, type = "text" creates a text input field, while type = "password" creates a password input field that hides the entered characters.
//             Example:


//     < label for= "username" > Username:</ >
//         <input type="text" id="username" name="username">

//             <label for="password">Password:</label>
//             <input type="password" id="password" name="password">
//                 Div: The <div> tag is a container tag that is used to group other HTML elements together. It is often used to apply styles to a group of elements, or to manipulate them using JavaScript.
//                     Example:


//                     <div>
//                         <h1>Welcome to my website!</h1>
//                         <p>Here you can find all sorts of interesting content.</p>
//                     </div>
//                     Small: The <small> tag is used to indicate that the text within it is smaller in size than the surrounding text. It is often used to display copyright or legal information in a smaller font.
//                         Example:


//                         <small>&copy; 2023 MyWebsite.com</small>
//                         Br: The <br> tag is used to create a line break in the text. It is a self-closing tag, meaning it does not require a closing tag.
//                             Example:


//                             <p>This is some text.<br>
//                                 This is on a new line.</p>