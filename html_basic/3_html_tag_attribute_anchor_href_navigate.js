// HTML(Hypertext Markup Language) is a markup language used for creating web pages.HTML documents are composed of HTML tags, which define the structure and content of the web page.

// An HTML tag consists of an opening tag, content, and a closing tag.Attributes are added to the opening tag to provide additional information about the tag.

// One common HTML tag is the anchor tag, or a tag, which is used to create links to other web pages or resources.The href attribute is used in the a tag to specify the URL or file path to the resource being linked to.

// Here is an example of an a tag used to create a link to the Google website:


//     < a href = "https://www.google.com" > Go to Google</ >
//         When a user clicks on the link, the web browser will navigate to the URL specified in the href attribute.