// Lists in HTML are used to group and organize related content.There are two types of lists: ordered and unordered.

// Unordered Lists
// Unordered lists are used to create a list of items where the order of the items is not important.To create an unordered list, we use the < ul > tag, and each item in the list is enclosed in a < li > tag.Here's an example:


//     < ul >
//   <li>Item 1</li>
//   <li>Item 2</li>
//   <li>Item 3</li>
// </ >
//     This will create an unordered list with three items:

// Item 1
// Item 2
// Item 3
// Ordered Lists
// Ordered lists are used to create a list of items where the order of the items is important.To create an ordered list, we use the < ol > tag, and each item in the list is enclosed in a < li > tag.Here's an example:


//     < ol >
//   <li>First item</li>
//   <li>Second item</li>
//   <li>Third item</li>
// </ >
//     This will create an ordered list with three items:

// First item
// Second item
// Third item
// Nested Lists
// Lists can also be nested inside of each other.Here's an example of a nested unordered list:


//     < ul >
//   <li>Item 1</li>
//   <li>Item 2
//     <ul>
//       <li>Sub-item 1</li>
//       <li>Sub-item 2</li>
//     </ul>
//   </li>
//   <li>Item 3</li>
// </ >
//     This will create an unordered list with three items, where the second item has a nested unordered list:

// Item 1
// Item 2
// Sub - item 1
// Sub - item 2
// Item 3
// Container Tag
// A container tag is used to group related elements together.The most common container tag is the < div > tag.Here's an example of using a div tag to group two list elements together:


//     < div >
//     <ul>
//         <li>Item 1</li>
//         <li>Item 2</li>
//     </ul>
// </ >
